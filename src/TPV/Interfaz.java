    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(!jcb.getModel().getSelectedItem().equals("Seleccione un producto")) {
            String nombre = (String)jcb.getModel().getSelectedItem();
            float precio = Consultas.consultaPrecio(nombre);
            int cantidad = (int) spin.getValue();
            Producto producto = new Producto(Integer.parseInt(lblID.getText()),nombre,precio);
            
            DefaultTableModel modelo = (DefaultTableModel)tabla.getModel();
            Object[] fila = new Object[5];
            fila[0] = producto.getId();
            fila[1] = producto.getNombre();
            fila[2] = cantidad;
            fila[3] = producto.getPrecio();
            fila[4] = producto.getPrecio()*cantidad;
            modelo.addRow(fila);
            float preciototal = 0;
            for (int i=0; i<modelo.getRowCount(); i++) {
                preciototal += (float) modelo.getValueAt(i, 4);
            }
            float round =(float) ((float) Math.round(preciototal * 100.0)/100.0);
            lblTotal.setText(Float.toString(round));
            
            Consultas.addCompra(producto.getId(), Integer.parseInt(lblTicket.getText()), cantidad);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        DefaultTableModel modelo = (DefaultTableModel)tabla.getModel();
        int r = tabla.getSelectedRow();
        if(r<0) {
            JOptionPane.showMessageDialog(this,"Debe seleccionar un registro");
        }else{
            Consultas.delCompra((int)modelo.getValueAt(r, 0), Integer.parseInt(lblTicket.getText()));
            modelo.removeRow(r);
        }
        float preciototal = 0;
        for (int i=0; i<modelo.getRowCount(); i++) {
            preciototal += (float) modelo.getValueAt(i, 3);
        }
        float round =(float) ((float) Math.round(preciototal * 100.0)/100.0);
        lblTotal.setText(Float.toString(round));
    }//GEN-LAST:event_btnDeleteActionPerformed

