    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(!txtNombre.getText().isEmpty() && !txtEdad.getText().isEmpty() && !txtSexo.getText().isEmpty() && !txtPeso.getText().isEmpty() && !txtAltura.getText().isEmpty()){
            try{
                short edad = Short.parseShort(txtEdad.getText());
                float peso = Float.parseFloat(txtPeso.getText());
                float altura = Float.parseFloat(txtAltura.getText());
                char sexo = txtSexo.getText().charAt(0);
                persona = new Persona(txtNombre.getText(),edad,sexo,peso,altura);
                modelo.add(persona);
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "Edad errÃ³nea");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Todos los campos deben estar rellenos");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        if(lista.getSelectedIndex()!=-1){
            modelo.remove(lista.getSelectedIndex());
        }
        txtNombre.setText("");
        txtEdad.setText("");
        txtSexo.setText("");
        txtPeso.setText("");
        txtAltura.setText("");
        lblNombre.setText("---------------------------");
        lblEdad.setText("---------------------------");
        lblSexo.setText("---------------------------");
        lblDNI.setText("-----------------");
        lblPeso.setText("-----------------");
        lblAltura.setText("-----------------");
        lblMayorEdad.setText("----------");
    }//GEN-LAST:event_btnDelActionPerformed

    private void btnModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModActionPerformed
        if(!txtNombre.getText().isEmpty() && !txtEdad.getText().isEmpty() && !txtSexo.getText().isEmpty() && !txtPeso.getText().isEmpty() && !txtAltura.getText().isEmpty() && lista.getSelectedIndex()!=-1){
            try{
                short edad = Short.parseShort(txtEdad.getText());
                float peso = Float.parseFloat(txtPeso.getText());
                float altura = Float.parseFloat(txtAltura.getText());
                char sexo = txtSexo.getText().charAt(0);
                persona = new Persona(txtNombre.getText(),edad,sexo,peso,altura);
                modelo.set(lista.getSelectedIndex(),persona);
                this.setLabel();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "Edad errÃ³nea");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Todos los campos deben estar rellenos");
        }
    }//GEN-LAST:event_btnModActionPerformed

    private void setLabel(){
        Persona p = (Persona) modelo.getElementAt(lista.getSelectedIndex());
        lblNombre.setText(p.getNombre());
        lblEdad.setText(Short.toString(p.getEdad()));
        lblSexo.setText(Character.toString(p.getSexo()));
        lblDNI.setText(p.getDni());
        lblPeso.setText(Float.toString(p.getPeso()));
        lblAltura.setText(Float.toString(p.getAltura()));
        if(p.esMayorDeEdad()==true){
            lblMayorEdad.setText("SÃ­");
        }else{
            lblMayorEdad.setText("No");
        }
    }
    
