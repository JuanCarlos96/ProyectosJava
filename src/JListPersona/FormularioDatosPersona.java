    private void btnIMCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIMCActionPerformed
        Persona p = (Persona) modelo.getElementAt(lista.getSelectedIndex());

        if(Float.parseFloat(lblPeso.getText())==p.getPeso() && Float.parseFloat(lblAltura.getText())==p.getAltura()){
            int num = p.calcularIMC();
            if(num==1){
                JOptionPane.showMessageDialog(this, "Tiene sobrepeso");
            }else{
                if(num==-1){
                    JOptionPane.showMessageDialog(this, "EstÃ¡ por debajo del peso ideal");
                }else{
                    JOptionPane.showMessageDialog(this, "Â¡EstÃ¡ en su peso ideal!");
                }
            }
            float imc,m;
            m = p.getAltura()/100;
            imc = p.getPeso()/(m*m);
            lblIMC.setText(Double.toString(imc));
        }
    }//GEN-LAST:event_btnIMCActionPerformed

